# λ§μ•½ λ©ν‘κ°’(s)μ΄ 0μΈ κ²½μ°, μ•„λ¬΄κ²ƒλ„ μ„ νƒν•μ§€ μ•μ€ 'κ³µμ§‘ν•©(X, X, X...)'λ„ ν•©μ΄ 0μ΄ λΌμ„ μΉ΄μ΄νΈκ°€ μ¬λΌκ°‘λ‹λ‹¤.
# ν•μ§€λ§ λ¬Έμ μ—μ„ "ν¬κΈ°κ°€ μ–‘μμΈ(μµμ† 1κ° μ΄μƒ κ³ λ¥Έ) λ¶€λ¶„μμ—΄"μ΄λΌκ³  ν–μΌλ―€λ΅, μ΄ κ°€μ§ μ •λ‹µ 1κ°λ¥Ό λΉΌμ¤μ•Ό ν•©λ‹λ‹¤!
if s == 0:
    result -= 1
