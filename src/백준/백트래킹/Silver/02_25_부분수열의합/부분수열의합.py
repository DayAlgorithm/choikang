import sys
input = sys.stdin.readline

n, s = map(int, input().split())
nums = list(map(int, input().split()))
result = 0 # μ •λ‹µ κ°μλ¥Ό κΈ°λ΅ν•  μ „μ—­ λ³€μ μ „κ΄‘ν!

# depth: ν„μ¬ ν™•μΈν•  μ«μμ μΈλ±μ¤
# current_sum: μ§€κΈκΉμ§€ μ„ νƒν• μ«μλ“¤μ ν•©
def dfs(depth, current_sum):
    global result
    # 1. νƒμ¶ μ΅°κ±΄: λ°°μ—΄μ λ(n)κΉμ§€ λ¨λ“  μ«μμ— λ€ν•΄ O/X μ„ νƒμ„ λ§μ³¤μ„ λ•
    if depth == n:
        # λκΉμ§€ μ™€μ„ ν™•μΈν•΄ λ³΄λ‹, μ§€κΈκΉμ§€μ ν•©μ΄ λ©ν‘κ°’(s)μ΄λ‘ λ‘κ°™λ„¤?
        if current_sum == s:
            result += 1 # μ •λ‹µ 1κ° μ¶”κ°€!
        return # λ” μ΄μƒ ν™•μΈν•  μ«μκ°€ μ—†μΌλ‹ λλμ•„κ°(Backtrack)
    # 2. O/X ν€΄μ¦ (λ°±νΈλν‚Ήμ ν•µμ‹¬!)
    # μ„ νƒμ§€ O: μ΄λ² μ«μλ¥Ό λ‚΄ λ°°λ‚­μ— λ„£λ”λ‹¤! (ν•©μ— λ”ν•΄μ¤)
    dfs(depth + 1, current_sum + nums[depth])
    # μ„ νƒμ§€ X: μ΄λ² μ«μλ¥Ό λ‚΄ λ°°λ‚­μ— μ• λ„£λ”λ‹¤! (ν•©μ„ κ·Έλ€λ΅ λ‘ )
    dfs(depth + 1, current_sum)
# 0λ²μ§Έ μΈλ±μ¤λ¶€ν„°, ν„μ¬ ν•©κ³„ 0μΌλ΅ νƒμƒ‰ μ‹μ‘!
dfs(0, 0)
